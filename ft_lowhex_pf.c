#include "ft_printf.h"

int	ft_lowhex_pf(int i)
{
	char	*str;
	str = "0123456789abcdef";
	if (i >= 16)
		ft_lowhex_pf(i / 16);
	return (write(1, &str[i % 16], 1));
}
