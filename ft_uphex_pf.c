#include "ft_printf.h"

int	ft_uphex_pf(int i)
{
	char	*str;

	str = "0123456789ABCDEF";
	if (i >= 16)
		ft_uphex_pf(i / 16);
	return (write(1, &str[i % 16], 1));
}
