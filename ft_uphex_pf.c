#include "ft_printf.h"

int	ft_uphex_pf(int i)
{
	int	count;
	
	count = 0;
	if (i >= 16)
		count += ft_uphex_pf(i / 16);
	return (count += write(1, &UP_HEX[i % 16], 1));
}
